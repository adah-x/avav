namespace AVAVLib
{
	/// <summary>
	/// Mögliche Geschlechter entsprechend der aktuellen Empfehlung
	/// </summary>
	public enum Gender
	{
		/// <summary>
		/// Männlich
		/// </summary>
		Male,
		/// <summary>
		/// Weiblich
		/// </summary>
		Female,
		/// <summary>
		/// Zu einem anderen Geschlecht gehörend.
		/// </summary>
		Diverse,
		/// <summary>
		/// Möchte keine Angaben bezüglich einer Geschlechterzuordnung machen.
		/// </summary>
		NotGiven,
		/// <summary>
		/// Das Geschlecht ist unbekannt.
		/// </summary>
		Unknown
	}
}